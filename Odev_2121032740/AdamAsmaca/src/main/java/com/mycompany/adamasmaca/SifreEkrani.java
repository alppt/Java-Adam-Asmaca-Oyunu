    private void logYaz(String islem) {
        try {
            java.time.LocalDateTime simdi = java.time.LocalDateTime.now();
            java.time.format.DateTimeFormatter format = java.time.format.DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");
            String tarihSaat = simdi.format(format);
            String logMesaji = tarihSaat + " -> " + islem + "\n";
            FileWriter fw = new FileWriter(logDosyaYolu, true);
            fw.write(logMesaji);
            fw.close();
        } catch (Exception e) {
            System.out.println("Log dosyasına yazılamadı: " + e.getMessage());
        }
    }
