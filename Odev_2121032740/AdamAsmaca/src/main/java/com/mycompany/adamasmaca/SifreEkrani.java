    private void btnGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirisActionPerformed
        // TODO add your handling code here:
        String girilenSifre = new String(txtSifre.getPassword());

        if (girilenSifre.isEmpty()) {
            lblMesaj.setText("Lütfen şifre girin!");
            return;
        }

        File sifreDosyasi = new File(sifreDosyaYolu);

        try {
            if (!sifreDosyasi.exists()) {
                FileWriter fw = new FileWriter(sifreDosyaYolu, true);
                fw.write(girilenSifre);
                fw.close();
                logYaz("İlk şifre oluşturuldu.");
                lblMesaj.setText("Şifre kaydedildi.");
                
                AnaOyunEkrani anaEkran = new AnaOyunEkrani();
                anaEkran.setVisible(true);
                this.dispose(); 
                
            } else {
                Path filePath = sifreDosyasi.toPath();
                List<String> satirlar = Files.readAllLines(filePath, Charset.defaultCharset());
                String kayitliSifre = satirlar.get(0);
                
                if (girilenSifre.equals(kayitliSifre)) {
                    logYaz("Başarılı giriş.");
                    AnaOyunEkrani anaEkran = new AnaOyunEkrani();
                    anaEkran.setVisible(true);
                    this.dispose();
                } else {
                    denemeSayisi++;
                    logYaz("Hatalı giriş denemesi: " + denemeSayisi);
                    if (denemeSayisi >= 3) {
                        JOptionPane.showMessageDialog(this, "3 Kez Hatalı Giriş!");
                        System.exit(0);
                    } else {
                        lblMesaj.setText("Kalan hak: " + (3 - denemeSayisi));
                        txtSifre.setText("");
                    }
                }
            }
        } catch (Exception ex) {
            lblMesaj.setText("Hata!");
        }
    }//GEN-LAST:event_btnGirisActionPerformed

